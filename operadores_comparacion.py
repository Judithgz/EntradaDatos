from traceback import print_tb

print('*** Operadores Comparacion (Relacionales) ***')
a, b = 7, 5
print(f'Valor inicial a: {a}, b: {b}')

# Operador igualdad ==
resultado = (a == b)
print(f'Resultado a == b: {resultado}')

# Operador diferente !=
resultado = (a != b)
print(f'Resultado a != b: {resultado}')

# Operador mayor que >
resultado = a > b
print(f'Resultado a > b: {resultado}')

# Operador mayor o igual que >=
resultado = a >= b
print(f'Resultado a >= b: {resultado}')

# Operador menor que
resultado = a < b
print(f'Resultado a < b: {resultado}')

# Operador menor o igual que <=
resultado = a <= b
print(f'Resultado a <= b: {resultado}')









